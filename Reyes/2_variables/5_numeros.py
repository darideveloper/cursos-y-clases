# Operaciones metamáticas
print (10 + 5.5) # 15.5
print (10 - 5) # 5
print (10 * 5) # 50
print (10 / 5.5) # 2.0
print (10 // 5) # 2
print (10 ** 5) # 100,000
print (11 % 5) # 1

# Jerarquía de operaciones
print (2 + 3 * 4) # 14
print ((2 + 3) * 4) # 20

# Operaciones con decimales
print (0.1 + 0.2) # 0.30000000000000004
print (3 * 0.1) # 0.30000000000000004

# Convertir a string
edad = 40
print ("Hola, Javier, feliz cumpleaños " + str(edad))
print (f"Hola, Javier, feliz cumpleaños {edad}")