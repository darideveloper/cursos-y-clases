# Condiciones lógicas: igualdad y desigaldad

"hola" == "hola" # True - Comparación o igualdad
"José" == "María" # False - Comparación o igualdad
10 == 10 # True - Comparación o igualdad
25 == 30 # False - Comparación o igualdad

"hola" != "hola" # False - Diferencia
"José" != "María" # True - Diferencia
10 != 10 # False - Diferencia
25 != 30 # True - Diferencia

# Comparaciones aritmeticas

10 > 18 # False - Mayor que
10 >= 18 # False - Mayor o igual que
10 == 18 # False - Igual que
10 <= 18 # True - Menor o igual que
10 < 18 # True - Menor que

# Multiples condiciones

True and True # True
True and False # False
False and True # False
False and False # False

True or True # True
True or False # True
False or True # True
False or False # False
