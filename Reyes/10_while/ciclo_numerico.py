# Numeros del 1 al 10 con for
print ("\nfor:")
for contador in range (1, 11):
    print (contador) 
    
# Numeros del 1 al 10 con while
print ("\nwhile:")
contador = 0 # contador inicial en 0
while (contador < 10): # mientras contador sea menor o igual a 10
    contador += 1 # aumenta el contador en 1
    print (contador) # imprime el contador