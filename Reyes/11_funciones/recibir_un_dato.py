# Función que recibe un dato (y especificar el tipo)
def saludo (usuario:str):
    """ Mostrar un saludo general de varias linea

    Args:
        usuario (str): Nombre del usuario
    """
    
    print (f"Hola {usuario}")
    print ("¿Cómo estás?")
    print ("Espero que bien")
    print ("Hasta la proxima\n")

# Llamar a la función con un dato
saludo ("Maria")

# Llamar a la función con otro dato
saludo ("Dari")

# Llamar a la función con otro dato
saludo ("José")

