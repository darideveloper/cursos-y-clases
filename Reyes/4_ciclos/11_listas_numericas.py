# Crear lista numerica
numeros = list(range(1, 10)) # [1, 2, 3, 4, 5, 6, 7, 8, 9]
numeros.reverse() # [9, 8, 7, 6, 5, 4, 3, 2, 1]

# Ejecutar algo un determinado numero de veces
for intento in range(1, 4): # [1, 2, 3]
    print ("Intentando conectar a la base de datos...")
    
# Ejecutar algo un determinado numero de veces (simplificando el ramge)
for intento in range(3): # [0, 1, 2]
    print ("Intentando conectar a la base de datos...")
    
# Ejecutar algo un determinado numero de veces (simplificando el for)
for _ in range(3): # [0, 1, 2]
    print ("Intentando conectar a la base de datos...")