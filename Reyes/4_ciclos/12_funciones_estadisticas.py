calificaciones = [10, 9, 8, 10, 7, 6, 4, 7, 10, 9, 8, 7]

# Numero máximo
print (max (calificaciones)) # 10

# Numero mínimo
print (min (calificaciones)) # 4

# Suma de los elementos de una lista
print (sum (calificaciones)) # 95

# Canidad de elementos de una lista
print (len (calificaciones)) # 12

# Promedio de los elementos de una lista
print (sum (calificaciones) / len (calificaciones)) # 7.91
