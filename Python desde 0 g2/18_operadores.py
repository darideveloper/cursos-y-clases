# Comparaciones de textos
texto_a = "Hola"
texto_b = "hola"
texto_a == texto_b # False # Comprar si son iguales
texto_a != texto_b # True # Comprar si son diferentes
texto_a.lower() == texto_b.lower() # True

texto_a = "   mundo "
texto_b = "mundo"
texto_a == texto_b # False
texto_a.strip() == texto_b.strip() # True

# Comparaciones de números
edad = 18
edad == 18 # True # comparar si son iguales
edad != 18 # False # comparar si son diferentes
edad > 10 # True # comparar si es mayor
edad < 10 # False # comparar si es menor
edad >= 10 # True # comparar si es mayor o igual
edad <= 10 # False # comparar si es menor o igual